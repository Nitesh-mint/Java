public class switchStatement implements Cloneable{
    public static void main(String args[]){
        int num = 2;
        switch (num){
            case 0:
            System.out.println("The value is 0");
            break;

            case 1:
            System.out.println("The value is 1");
            break;

            case 2:
            System.out.println("The value is 2.");
            break;
        }
    }
    
}
