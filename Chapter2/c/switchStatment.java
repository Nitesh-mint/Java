//switch are used when there are too many if else cases.
public class switchStatment {
    public static void main(String args[]){    
        int expression = 5;
        switch(expression){
            case 0:
                System.out.println("0");
                break;
            case 1:
                System.out.println("0");
                break;
            default:
                System.out.println("default");
                break;
        }
    }
    
    
}
