public class IncrementDecrement {
    public static void main(String args[]){
        int a=1;
        System.out.println("a is:" +a); //defatult 1.
        System.out.println("++a is:"+ ++a); //pre-increment
        System.out.println("a++ is:"+ a++); //post-increment
        System.out.println("a is:" +a);
        System.out.println("--a is:"+ --a); //pre-decrement
        System.out.println("a-- is:"+ a--);    //post-decrement
    }
}
